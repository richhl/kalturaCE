					<h3><span>2.4</span>TRANSCODING SETTING</h3></div>
				<p>To ensure optimal playback, source files uploaded to the system will be transcoded into multiple file flavors.<br />
						The player will automatically select the most appropriate file flavor based on multiple parameters such as the bandwidth connection of the viewer, player size, etc.<br />
						File flavors can also be used for download and syndication.&nbsp; By default, files uploaded to the system will be transcoded to the flavors selected in the main transcoding settings page - this is considered the "Default Transcoding Profile?.<br />
						Terminology:<br />
						Source file - the file uploaded as a source<br />
						Flavor - a single output file with its specific file type, bitrate, gop size, etc.&nbsp; could be either used for playback, download or editing.<br />

						For every file uploaded, the system tries to convert it to all selected default flavors.&nbsp; If the source file is in low quality, low bitrate, or small dimensions, the system will convert only to flavors that are applicable for this source file.&nbsp; For example if the file bitrate is 300 Kbps, the system will not create an additional flavor for HD (that requires over 4000 kbps).&nbsp; Transcoding only to the relevant flavors, allows you to minimize your storage requirements, and reduce storage costs.
				</p><br />

				<div class="dataTitle" id="contentSection231">
					<a href="#main">Back to top</a>
					<h4><span>2.4.1</span>Advanced Mode (multiple transcoding profiles)</h4>
				</div>
				<p>Advanced mode allows you further flexibility and control over flavors created.&nbsp; You can specify multiple conversion profiles; each transcoding profile is a group of flavors.&nbsp; For each upload session, you can select the transcoding profile to be used (done via flashvar passed to the uploader widget - KCW).&nbsp;
						   For example, a website that holds both UGC and editorial content can specify a different transcoding profile for each content type.&nbsp; UGC content will be associated with a transcoding profile that contains only a single flavor - Standard web quality.&nbsp; Editorial content will be associated with a transcoding profile that contains multiple flavors ranging from Standard quality to HD.
				</p><br />



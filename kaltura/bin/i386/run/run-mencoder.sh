#!/bin/bash
KALTURA_BIN="$(cd "${0%/*}" 2>/dev/null; echo "$PWD")"
KALTURA_BIN_DIRS=$KALTURA_BIN/latest-dir
KALTURA_BIN_FFMPEG=$KALTURA_BIN_DIRS/ffmpeg
KALTURA_BIN_MENCODER=$KALTURA_BIN_DIRS/mencoder
KALTURA_BIN_MENCODER_CODECS=$KALTURA_BIN_DIRS/mencoder/codecs
MPLAYER_HOME=$KALTURA_BIN_MENCODER
export MPLAYER_HOME
LD_LIBRARY_PATH=$KALTURA_BIN_MENCODER:$KALTURA_BIN_FFMPEG:$KALTURA_BIN_MENCODER_CODECS $KALTURA_BIN_MENCODER/mencoder "$@"
